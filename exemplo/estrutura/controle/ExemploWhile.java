package exemplo.estrutura.controle;

public class ExemploWhile {

	public static void main(String[] args) {
		// criando uma variavel para construção do loop while
		int num = 0;
		
		// construindo o loop
		while(num < 10) {
			// bloco que será executado enquanto a condição for verdadeira
			
			num++;
			System.out.println("O valor de num é: " + num);
			
			
		}

	}

}
