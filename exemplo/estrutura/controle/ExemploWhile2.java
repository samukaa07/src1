package exemplo.estrutura.controle;

public class ExemploWhile2 {

	public static void main(String[] args) {
		// declarar uma variavel com um valor inicial para operar o loop
		int num = 15;
		// construção do loop do/while
		do {
		// operação executada ao menos uma vez
					
			System.out.println("O valor de num é: " + num);
			num++;
		}while(num < 15);
			
		}
	}
	

